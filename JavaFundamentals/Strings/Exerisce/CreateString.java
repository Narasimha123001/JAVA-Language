package com.JavaFundamentals.Strings.Exerisce;

public class CreateString {
    public static void main(String[] args) {
        // using literals
        String str1 = "Hello";
        System.out.println("The string creations using Strings literals :" + str1);

        //using new keyword
        String str2 = new String("Hello World !");
        System.out.println("the string creation using new Keyword : "+ str2);
    }
}
